package org.icroco.cube;

import com.gluonhq.charm.down.Platform;
import com.gluonhq.charm.down.Services;
import com.gluonhq.charm.down.plugins.LifecycleService;
import com.gluonhq.charm.glisten.application.MobileApplication;
import com.gluonhq.charm.glisten.control.Avatar;
import com.gluonhq.charm.glisten.control.NavigationDrawer;
import com.gluonhq.charm.glisten.control.NavigationDrawer.Item;
import com.gluonhq.charm.glisten.control.NavigationDrawer.ViewItem;
import com.gluonhq.charm.glisten.visual.MaterialDesignIcon;
import javafx.scene.Node;
import javafx.scene.image.Image;

import static org.icroco.cube.CubeApplication.MENU_LAYER;

public class DrawerManager {

    private final NavigationDrawer drawer;

    public DrawerManager() {
        this.drawer = new NavigationDrawer();
        
        NavigationDrawer.Header header = new NavigationDrawer.Header("500 LEDs Cube",
                "Cube Light Show",
                new Avatar(21, new Image(DrawerManager.class.getResourceAsStream("/cube-color-64.png"))));
        drawer.setHeader(header);
        
        final Item demoItem = new ViewItem("Demo", MaterialDesignIcon.PLAY_CIRCLE_OUTLINE.graphic(), CubeApplication.DEMO_VIEW);
        final Item musicItem = new ViewItem("Music", MaterialDesignIcon.MUSIC_NOTE.graphic(), CubeApplication.EQUALIZER_VIEW);
        final Item textItem = new ViewItem("Message", MaterialDesignIcon.TEXTSMS.graphic(), CubeApplication.TEXT_VIEW);
        final Item gameItem = new ViewItem("Demo", MaterialDesignIcon.GAMES.graphic(), CubeApplication.GAMES_VIEW);
        drawer.getItems().addAll(demoItem, musicItem, textItem, gameItem);

        if (Platform.isDesktop()) {
            final Item quitItem = new Item("Quit", MaterialDesignIcon.EXIT_TO_APP.graphic());
            quitItem.selectedProperty().addListener((obs, ov, nv) -> {
                if (nv) {
                    Services.get(LifecycleService.class).ifPresent(LifecycleService::shutdown);
                }
            });
            drawer.getItems().add(quitItem);
        }
        
        drawer.addEventHandler(NavigationDrawer.ITEM_SELECTED, e -> MobileApplication.getInstance().hideLayer(MENU_LAYER));
        
        MobileApplication.getInstance().viewProperty().addListener((obs, oldView, newView) -> updateItem(newView.getName()));
        updateItem(CubeApplication.BASIC_VIEW);
    }
    
    private void updateItem(String nameView) {
        for (Node item : drawer.getItems()) {
            if (item instanceof ViewItem && ((ViewItem) item).getViewName().equals(nameView)) {
                drawer.setSelectedItem(item);
                break;
            }
        }
    }
    
    public NavigationDrawer getDrawer() {
        return drawer;
    }
}
